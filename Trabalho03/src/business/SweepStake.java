package business;

public class SweepStake {

	private int id;
	private String name;
	private String CPF;
	
	public int getId() 
	{
		return id;
	}
	public void setId(int id) 
	{
		this.id = id;
	}
	public String getName() 
	{
		return name;
	}
	public void setName(String nome) 
	{
		this.name = nome;
	}
	public String getCPF() 
	{
		return CPF;
	}
	public void setCPF(String cPF) 
	{
		CPF = cPF;
	}
	
	
}
