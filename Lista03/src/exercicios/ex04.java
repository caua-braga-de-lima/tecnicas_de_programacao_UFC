package exercicios;

public class ex04 
{
	public static int somador(int vet[])
	{
		int soma = 0;
		for(int i = 0; i < vet.length; i++)
		{
			soma += vet[i];
		}
		return soma;
	}
	public static void main(String[] args) 
	{
		/*4) Somatório: Sua função deverá receber um vetor e retorna a soma de todos
				elementos do vetor;*/
		
		// vetor exemplo
			int vet[] =  {12, 13, 14, 15, 16, 17, 18, 19, 20, -3, -4, -5, -6 ,-7};
			
			System.out.println("A soma dos elementos do vetor é " + somador(vet));
			
			

	}

}
